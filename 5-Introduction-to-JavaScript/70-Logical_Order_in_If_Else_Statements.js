//Learned the importance of order in regards to if, else if statements and understanding that functions are executed from top to bottom so it is important to understand which statement to put first

function orderMyLogic(val) {
  if (val < 5) {
    return "Less than 5";
  } else if (val < 10) {
    return "Less than 10";
  } else {
    return "Greater than or equal to 10";
  }
}

// Change this value to test
orderMyLogic(7);
